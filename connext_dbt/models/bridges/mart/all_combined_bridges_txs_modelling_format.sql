SELECT
    tx.bridge,
    tx.id,
    tx.from_date AS timestamp_in,
    tx.to_date AS timestamp_out,
    tx.from_amount AS amount_in,
    tx.to_amount AS amount_out,
    tx.gas_symbol AS gas_fee_symbol,
    tx.gas_amount AS gas_fee,
    tx.gas_amount_usd AS gas_price_usd,
    tx.relay_symbol AS relay_fee_symbol,
    tx.relay_amount AS relay_fee,
    tx.relay_amount_usd AS relay_fee_price_usd,
    tx.from_token_address AS token_in,
    tx.from_token_symbol AS symbol_in,
    tx.to_token_address AS token_out,
    tx.to_token_symbol AS symbol_out,
    tx.from_amount_usd AS price_in_usd,
    tx.to_amount_usd AS price_out_usd,
    tx.from_tx_hash AS txn_hash_in,
    tx.to_tx_hash AS txn_hash_out,
    tx.from_token_address AS token_address_in,
    tx.to_token_address AS token_address_out,
    tx.from_chain_id AS chain_in,
    tx.from_chain_name AS chain_name_in,
    tx.to_chain_id AS chain_out,
    tx.to_chain_name AS chain_name_out,
    tx.from_user_address AS user_address_in,
    tx.to_user_address AS user_address_out
FROM {{ ref('all_combined_bridges_txs') }} AS tx
